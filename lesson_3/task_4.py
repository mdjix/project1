# 1 пример
x1 = 17 / 2 * 3 + 2
y1 = (17 / 2) * 3 + 2

if x1 == y1:
    print(f'{x1} = {y1}')
elif x1 > y1:
    print(f'{x1} > {y1}')
else:
    print(f'{x1} < {y1}')

